char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;
	int	size;

	i = 0;
	size = n;
	while (i < size && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < size)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
